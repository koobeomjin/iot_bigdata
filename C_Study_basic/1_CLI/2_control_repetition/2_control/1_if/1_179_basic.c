#include <stdio.h>

void main(){
	int num;
	printf("정수 입력: ");
	scanf("%d", &num);

	//if(상수/변수/표현식),상수나 변수는 0이 아닌 값
	//괄호안에 값이 참(1)이면 내부 명령(statement) 수행
	//거짓(0)이면 명령(statement block)을 수행하지 않고 빠져 나온다. 
	
	// num이 음수일 경우에 12번, 15번 라인이 불필요하게 수행되었다. 
	// 이는 개선이 필요하다.
	if(num<0){
			printf("입력 값은 0보다 작다. \n");
	}
	if(num>0){
			printf("입력 값은 0보다 크다. \n");
	}
	if(num==0){
			printf("입력 값은 0이다. \n");
	}


}

